package FFF;

public class Parent_class {
	
	/*Final is a keyword to declare constant values
	 *  when we pass final key word in the class "(public final class Parent_class)" then it is used to prevent Inheritance.
	 * when we pass final key word in the method "(public final void life())"  then it is used to prevent Method overriding.
	 * */
	
	public void job() {
		System.out.println("S/W job");
	}
	public void life()
	{
		System.out.println("thinking");
	}
}

package FFF;

public class final_ {

	public static void main(String[] args) {
		int i=20;
		i=30;
		System.out.println(i);
		i=50;
		System.out.println(i);
		
		
		/*
		 * Final is a keyword to define constant values 
		 * final key word is used to prevent Inheritance.
		 * */
		final int k= 70;
		System.out.println(k);

	}

}
